def eggDrop(n,k):
    T = [[0 for j in range(k+1)] for i in range(n+1)]
    for i in range(1,n+1):
        for j in range(1,k+1):
            if i==1 :
                T[i][j] = j
                
            elif i <=j:
                out=[]
                for p in range(1,j+1):
                    
                    out.append(1 + max(T[i-1][p-1],T[i][j-p]))
                T[i][j] = min(out)
            else:
                T[i][j] = T[i-1][j]
        
        
    return T[n][k]
    
    
    
    
