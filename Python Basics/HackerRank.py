def No_of_ways(arr,n):
    T = [[0 for j in range(n+1)] for i in range(len(arr))]
    
    for j in range(n+1):
        T[0][j] =1
    
    for i in range(1,len(arr)):
        for j in range(n+1):
            if j < arr[i]:
                T[i][j] = T[i-1][j]
            else:
                T[i][j] = T[i-1][j] + T[i][j - arr[i]]
        
        
        
    return T[len(arr)-1][n]
                               
