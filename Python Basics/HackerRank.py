def MinPalPart(str1):
    n = len(str1)
    T = [[0 for j in range(n)] for i in range(n)]
    
    for i in range(n):
        T[i][i] =0
    
    for l in range(2,n+1):
        j = n-l+1
        
        for  i in range(j):
            temp= str1[i:i+l]
            if temp == temp[::-1]:
                T[i][i+l-1] = 0
            else:
                res=[]
                for k in range(l-1):
                    res.append(1 + T[i][i+k] + T[i+k+1][i+l-1])
                    
                T[i][i+l-1] = min(res)
     
    return T[0][n-1]             

