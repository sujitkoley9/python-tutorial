def StaircaseDyn(s,m):
    T = [0]*(s+1)
    T[0]=1
    T[1]=1
    
    for i in range(2,(s+1)):
        for j in range(1,m+1):
            if j<=i:
                T[i] += T[i-j]
   
    return T[s]
        
    
    

