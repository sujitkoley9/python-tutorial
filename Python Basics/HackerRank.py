def knapsack(val,wt,w,n):
    T= [ [0 for j in range(w+1)] for i in range(n+1)]
    
    for i in range(n+1):
        for j in range(w+1):
            if i==0 or j==0:
                T[i][j]=0
            elif  wt[i-1] > j:
                T[i][j]= T[i-1][j]
                
            else:
                
                T[i][j] = max(val[i-1]+T[i-1][j-wt[i-1]],T[i-1][j])
            
    
    
    return T[n][w]
    

